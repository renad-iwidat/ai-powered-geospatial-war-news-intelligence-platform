"""
=============================================================================
Arabic Named Entity Recognition (NER) Service
=============================================================================
يستخرج أسماء الأماكن من النصوص العربية باستخدام Stanza NLP.

الخطوات:
1. تحميل Stanza pipeline للعربية
2. معالجة النص واستخراج كيانات NER
3. تصفية الكيانات من نوع LOC/GPE (أماكن)
4. تطبيع الأسماء (إزالة التشكيل، توحيد الحروف)
=============================================================================
"""

import re
import stanza
from typing import List


# ============================================================================
# Initialize Stanza Pipeline
# ============================================================================
# تحميل الـ pipeline مرة واحدة عند استيراد الملف
# يحتوي على tokenizer و NER للعربية
nlp = stanza.Pipeline(
    lang="ar",
    processors="tokenize,ner",
    tokenize_no_ssplit=True
)


# ============================================================================
# Helper Functions
# ============================================================================

def normalize_ar(s: str) -> str:
    """
    تطبيع النص العربي.
    
    الخطوات:
    1. إزالة التشكيل (الفتحة، الضمة، إلخ)
    2. توحيد الألف (أ، إ، آ -> ا)
    3. توحيد التاء المربوطة (ة -> ه)
    4. إزالة المسافات الزائدة
    
    Args:
        s: النص المراد تطبيعه
    
    Returns:
        النص المطبّع
    """
    s = (s or "").strip()
    
    # إزالة التشكيل (الحروف الصغيرة فوق/تحت الحروف)
    s = re.sub(r"[\u064B-\u0652]", "", s)
    
    # توحيد الألف
    s = s.replace("أ", "ا").replace("إ", "ا").replace("آ", "ا")
    
    # توحيد التاء المربوطة
    s = s.replace("ة", "ه")
    
    # إزالة المسافات الزائدة
    s = re.sub(r"\s+", " ", s)
    
    return s


# ============================================================================
# Main Extraction Function
# ============================================================================

def extract_places_ner(text: str) -> List[str]:
    """
    استخراج أسماء أماكن من النص العربي.
    
    يستخدم Stanza NER لاستخراج كيانات من نوع:
    - LOC: مواقع جغرافية (جبال، أنهار، إلخ)
    - GPE: كيانات جيوسياسية (دول، مدن، إلخ)
    
    Args:
        text: النص المراد استخراج الأماكن منه
    
    Returns:
        قائمة بأسماء الأماكن المستخرجة (مطبّعة وفريدة)
    """
    if not text or len(text.strip()) < 3:
        return []

    # معالجة النص باستخدام Stanza
    doc = nlp(text)
    places = []

    # استخراج الكيانات من نوع LOC أو GPE
    for ent in doc.ents:
        label = ent.type.upper()
        if label in ("LOC", "GPE"):
            # تطبيع اسم المكان
            val = normalize_ar(ent.text)
            if len(val) >= 2:
                places.append(val)

    # إزالة التكرارات مع الحفاظ على الترتيب
    out = []
    seen = set()
    for p in places:
        if p not in seen:
            seen.add(p)
            out.append(p)
    
    return out
